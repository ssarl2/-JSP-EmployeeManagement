package table;

public class ESCommute {

	protected int eno;
	protected String cin;
	protected String cout;
	
	public int getEno() {
		return eno;
	}
	public ESCommute setEno(int eno) {
		this.eno = eno;
		return this;
	}
	public String getCin() {
		return cin;
	}
	public ESCommute setCin(String cin) {
		this.cin = cin;
		return this;
	}
	public String getCout() {
		return cout;
	}
	public ESCommute setCout(String cout) {
		this.cout = cout;
		return this;
	}
	
}
