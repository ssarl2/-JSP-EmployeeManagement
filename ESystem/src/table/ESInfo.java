package table;

public class ESInfo {

	protected int eno;
	protected String ename;
	protected int power;
	protected String sex;
	protected int deptNo;
	protected String phone;
	protected String address;
	protected String joinDate;
	
	
	public int getEno() {
		return eno;
	}
	public ESInfo setEno(int eno) {
		this.eno = eno;
		return this;
	}
	public String getEname() {
		return ename;
	}
	public ESInfo setEname(String ename) {
		this.ename = ename;
		return this;
	}
	public int getPower() {
		return power;
	}
	public ESInfo setPower(int power) {
		this.power = power;
		return this;
	}
	public String getSex() {
		return sex;
	}
	public ESInfo setSex(String sex) {
		this.sex = sex;
		return this;
	}
	public int getDeptNo() {
		return deptNo;
	}
	public ESInfo setDeptNo(int deptNo) {
		this.deptNo = deptNo;
		return this;
	}
	public String getPhone() {
		return phone;
	}
	public ESInfo setPhone(String phone) {
		this.phone = phone;
		return this;
	}
	public String getAddress() {
		return address;
	}
	public ESInfo setAddress(String address) {
		this.address = address;
		return this;
	}
	public String getJoinDate() {
		return joinDate;
	}
	public ESInfo setJoinDate(String joinDate) {
		this.joinDate = joinDate;
		return this;
	}
	
}
