package table;

public class ESData {

	protected int fno;
	protected int power;
	protected String data;
	
	public int getFno() {
		return fno;
	}
	public ESData setFno(int dno) {
		this.fno = dno;
		return this;
	}
	public int getPower() {
		return power;
	}
	public ESData setPower(int power) {
		this.power = power;
		return this;
	}
	public String getData() {
		return data;
	}
	public ESData setData(String data) {
		this.data = data;
		return this;
	}
	
}
